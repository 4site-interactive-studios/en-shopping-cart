const BASE = "https://fonts.gstatic.com/s/e/notoemoji/latest";

export function getEmojiCodepoint(name: string): string | null {
  if (!name) return null;
  const key = name.toLowerCase().trim();
  return EMOJI_MAP[key] || null;
}

export function getStaticEmojiUrl(name: string): string | null {
  const code = getEmojiCodepoint(name);
  return code ? `${BASE}/${code}/emoji.svg` : null;
}

export function getAnimatedEmojiUrl(name: string): string | null {
  const code = getEmojiCodepoint(name);
  return code ? `${BASE}/${code}/512.webp` : null;
}
